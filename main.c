#include "push_swap.h"

int	main(int argc, char **argv)
{
	t_data	data;
	int		*temp;
	size_t		n = 0;

	init(&data);
	first_verifs(argc, argv);
	temp = parser(&data, argc, argv);
	if (is_list_valid(&data, temp) == 0)
		error_message(4);
	while (n < data.size_temp)
	{
		ft_printf("%d\n", temp[n]);
		n++;
	}
	free(temp);
	return (0);
}
